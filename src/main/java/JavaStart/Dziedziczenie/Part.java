package JavaStart.Dziedziczenie;

public class Part {
    public int idNumber;
    public String nameProducent;
    public String model;
    public double seriesOfProduct;


    public int getIdNumber() {
        return idNumber;
    }

    public void setIdNumber(int idNumber) {
        this.idNumber = idNumber;
    }

    public String getNameProducent() {
        return nameProducent;
    }

    public void setNameProducent(String nameProducent) {
        this.nameProducent = nameProducent;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public double getSeriesOfProduct() {
        return seriesOfProduct;
    }

    public void setSeriesOfProduct(double seriesOfProduct) {
        this.seriesOfProduct = seriesOfProduct;
    }
}
